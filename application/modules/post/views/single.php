				<h2><a href="#" class="post-title">年总结Summary</a></h2>
				<p>盖·里奇携着他的福尔摩斯第二部终于来了！在翘首等待了两年之后，众多腐女小基都无法压抑内心的激动之情，纷纷大批杀向电影院，楼主作为唐尼大眼睛和裘德·洛的脑残粉，随着大军一起去围观基情。</p>
				<p>电影上映前，宣传造势就十分凶猛，预告片在上映前好几个月就一直在影院轮轴儿拼命播，各种海报横幅电子屏也是铺天盖地。在《新年前夜》里，纽约的时代广场上最闪耀的，居然是唐尼在海报上那张脸！宣传商是不是也是脑残粉啊，激动成这样，宣传大手笔啊！</p><p></p>
				<p>撇开闪瞎钛金狗眼的基情，电影本身就已经非常优秀，悬疑情节设置精彩，剧情一直很引人入胜，129分钟不显得长。前段的赌场刺杀戏，动作戏份让人目不暇接，高速衔接眼花缭乱；火车上的枪战戏，智商和动作齐齐上阵，又不失幽默串场；而中段的爆破戏，更是用了慢速摄影，千金一掷做出来的视觉效果非常赞，超越《反抗军》的丛林枪战几千米，真是有史以来最漂亮的丛林枪战戏码！慢速爆破惊艳极了。而最后的瑞士雪山堡，那个景色真是漂亮，浮光掠影看到的白练般的瀑布，飞流直下三千尺的感觉瞬间出来了，电脑特效赞一个！单从摄影、剪辑、特效来说，该片完全满足一部娱乐大片应该给我们带来的期待！</p>
